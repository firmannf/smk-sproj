    public void showDataToLV() {
        try {
            Lmodel = new DefaultListModel();
            sql = "SELECT Name_Project FROM PROJECT";
            stat = con.createStatement();
            result = stat.executeQuery(sql);

            while (result.next()) {
                Lmodel.addElement(result.getString("Name_Project"));
            }

            jList1.setModel(Lmodel);
        } catch (SQLException ex) {
            Logger.getLogger(DlgOpen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

