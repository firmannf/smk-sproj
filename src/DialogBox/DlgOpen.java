    private void BtnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOpenActionPerformed

        // TODO add your handling code here:
        if (jList1.isSelectionEmpty() == true) {
            JOptionPane.showMessageDialog(null, "Select Project Correctly");
        } else {
            projectmanagement.main.setName_Project(jList1.getSelectedValue().toString());
            try {
                result = con.createStatement().executeQuery("SELECT Id_Project, Name_Project FROM project WHERE Name_Project = '" + projectmanagement.main.getName_Project() + "'");
                result.first();
                projectmanagement.main.setID_Project(result.getString(1));
            } catch (SQLException ex) {
                Logger.getLogger(DlgOpen.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            projectmanagement.main.getfrmmain();
            this.dispose();
            FrmSub fs = new FrmSub(projectmanagement.main.getName_Project());
            fs.setVisible(true);
        }
    }//GEN-LAST:event_BtnOpenActionPerformed

